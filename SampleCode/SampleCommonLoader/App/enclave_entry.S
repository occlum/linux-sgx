#include "enclave_entry.h"

.text
    .global sgx_get_token
sgx_get_token:
    EENTER_PROLOG

    mov $0x2, %rax
    mov %rsi, %rbx
    mov frame_arg0, %rdi
    lea_pic sgx_async_exit, %rcx
sgx_async_exit:
    enclu

    EENTER_EPILOG
    ret
/* We do not need executable stack.*/
.section .note.GNU-stack,"",@progbits
